#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char	*t;
	size_t	i;

	while (*s == ' ' || *s == '\t' || *s == '\n')
			s++;
	i = 0;
	t = ft_strnew(ft_strlen(s));
	if (t)
	{
		while (*s)
		{
			t[i] = *s;
			i++;
			s++;
		}
		i--;
		while (i != 0 && (t[i] == '\t' || t[i] == '\n' || t[i] == ' '))
			i--;
		t[++i] = '\0';
	}
	return (t);
}
